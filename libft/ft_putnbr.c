#include "libft.h"

void	ft_putnbr(int n)
{
	unsigned int	nbr;
	char			digit;

	if (n < 0)
	{
		write(1, "-", 1);
		n *= -1;
	}
	nbr = n;
	if (nbr >= 10)
		ft_putnbr(nbr / 10);
	digit = (nbr % 10) + '0';
	write(1, &digit, 1);
}
