#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	f;

	i = 0;
	f = c;
	while (s[i] != '\0' && s[i] != c)
	{
		i++;
	}
	if (s[i] == f)
		return ((char*)&s[i]);
	else
		return (NULL);
}
